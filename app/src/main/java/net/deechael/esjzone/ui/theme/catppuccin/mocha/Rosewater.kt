@Composable
fun CatppuccinMochaRosewaterTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaRosewaterLightColors
    } else {
        CatppuccinMochaRosewaterDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

