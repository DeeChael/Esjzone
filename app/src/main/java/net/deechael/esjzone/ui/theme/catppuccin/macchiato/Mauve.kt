@Composable
fun CatppuccinMacchiatoMauveTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoMauveLightColors
    } else {
        CatppuccinMacchiatoMauveDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

