@Composable
fun CatppuccinMochaGreenTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaGreenLightColors
    } else {
        CatppuccinMochaGreenDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

