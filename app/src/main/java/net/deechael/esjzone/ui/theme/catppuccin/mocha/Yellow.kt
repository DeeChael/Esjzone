@Composable
fun CatppuccinMochaYellowTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaYellowLightColors
    } else {
        CatppuccinMochaYellowDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

