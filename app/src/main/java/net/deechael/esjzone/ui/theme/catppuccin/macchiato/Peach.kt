@Composable
fun CatppuccinMacchiatoPeachTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoPeachLightColors
    } else {
        CatppuccinMacchiatoPeachDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

