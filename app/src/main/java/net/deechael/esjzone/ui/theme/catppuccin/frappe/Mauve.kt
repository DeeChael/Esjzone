@Composable
fun CatppuccinFrappeMauveTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeMauveLightColors
    } else {
        CatppuccinFrappeMauveDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

