@Composable
fun CatppuccinFrappePeachTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappePeachLightColors
    } else {
        CatppuccinFrappePeachDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

