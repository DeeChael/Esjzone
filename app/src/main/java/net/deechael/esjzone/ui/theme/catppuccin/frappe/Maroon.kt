@Composable
fun CatppuccinFrappeMaroonTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeMaroonLightColors
    } else {
        CatppuccinFrappeMaroonDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

