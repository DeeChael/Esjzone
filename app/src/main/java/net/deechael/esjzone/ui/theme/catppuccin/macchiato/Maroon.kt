@Composable
fun CatppuccinMacchiatoMaroonTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoMaroonLightColors
    } else {
        CatppuccinMacchiatoMaroonDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

