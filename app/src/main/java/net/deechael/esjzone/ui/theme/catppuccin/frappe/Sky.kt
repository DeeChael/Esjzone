@Composable
fun CatppuccinFrappeSkyTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeSkyLightColors
    } else {
        CatppuccinFrappeSkyDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = HarmonyOsSansSCTypography,
        content = content
    )
}

