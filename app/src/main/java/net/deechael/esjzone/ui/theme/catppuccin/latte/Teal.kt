@Composable
fun CatppuccinLatteTealTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteTealLightColors
    } else {
        CatppuccinLatteTealDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

