@Composable
fun CatppuccinMacchiatoTealTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoTealLightColors
    } else {
        CatppuccinMacchiatoTealDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

