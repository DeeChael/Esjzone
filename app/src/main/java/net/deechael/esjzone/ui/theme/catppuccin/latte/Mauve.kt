@Composable
fun CatppuccinLatteMauveTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteMauveLightColors
    } else {
        CatppuccinLatteMauveDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

