@Composable
fun CatppuccinLatteSkyTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteSkyLightColors
    } else {
        CatppuccinLatteSkyDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = HarmonyOsSansSCTypography,
        content = content
    )
}

