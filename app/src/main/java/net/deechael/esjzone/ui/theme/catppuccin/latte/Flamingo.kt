@Composable
fun CatppuccinLatteFlamingoTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteFlamingoLightColors
    } else {
        CatppuccinLatteFlamingoDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}