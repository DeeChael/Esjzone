@Composable
fun CatppuccinMochaFlamingoTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaFlamingoLightColors
    } else {
        CatppuccinMochaFlamingoDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

