@Composable
fun CatppuccinFrappePinkTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappePinkLightColors
    } else {
        CatppuccinFrappePinkDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

