@Composable
fun CatppuccinFrappeLavenderTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeLavenderLightColors
    } else {
        CatppuccinFrappeLavenderDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

