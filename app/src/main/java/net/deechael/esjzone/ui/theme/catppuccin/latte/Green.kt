@Composable
fun CatppuccinLatteGreenTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteGreenLightColors
    } else {
        CatppuccinLatteGreenDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = HarmonyOsSansSCTypography,
        content = content
    )
}

