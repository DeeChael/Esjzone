@Composable
fun CatppuccinLatteRedTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteRedLightColors
    } else {
        CatppuccinLatteRedDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = HarmonyOsSansSCTypography,
        content = content
    )
}

