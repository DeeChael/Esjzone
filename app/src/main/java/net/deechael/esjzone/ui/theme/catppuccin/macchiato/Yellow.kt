@Composable
fun CatppuccinMacchiatoYellowTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoYellowLightColors
    } else {
        CatppuccinMacchiatoYellowDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

