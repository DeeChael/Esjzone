@Composable
fun CatppuccinLattePeachTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLattePeachLightColors
    } else {
        CatppuccinLattePeachDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = HarmonyOsSansSCTypography,
        content = content
    )
}

