@Composable
fun CatppuccinMochaRedTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaRedLightColors
    } else {
        CatppuccinMochaRedDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

