@Composable
fun CatppuccinFrappeYellowTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeYellowLightColors
    } else {
        CatppuccinFrappeYellowDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

