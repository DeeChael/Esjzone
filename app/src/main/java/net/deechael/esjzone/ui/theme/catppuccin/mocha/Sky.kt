@Composable
fun CatppuccinMochaSkyTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaSkyLightColors
    } else {
        CatppuccinMochaSkyDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

