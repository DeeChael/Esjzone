@Composable
fun CatppuccinMochaBlueTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaBlueLightColors
    } else {
        CatppuccinMochaBlueDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

