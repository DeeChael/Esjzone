@Composable
fun CatppuccinLatteSapphireTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinLatteSapphireLightColors
    } else {
        CatppuccinLatteSapphireDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

