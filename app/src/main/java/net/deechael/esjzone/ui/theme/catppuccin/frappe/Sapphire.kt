@Composable
fun CatppuccinFrappeSapphireTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeSapphireLightColors
    } else {
        CatppuccinFrappeSapphireDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

