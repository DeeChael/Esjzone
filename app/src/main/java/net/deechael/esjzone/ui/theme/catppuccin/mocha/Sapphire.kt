@Composable
fun CatppuccinMochaSapphireTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMochaSapphireLightColors
    } else {
        CatppuccinMochaSapphireDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

