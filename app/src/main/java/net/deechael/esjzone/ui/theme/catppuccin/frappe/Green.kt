@Composable
fun CatppuccinFrappeGreenTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinFrappeGreenLightColors
    } else {
        CatppuccinFrappeGreenDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

