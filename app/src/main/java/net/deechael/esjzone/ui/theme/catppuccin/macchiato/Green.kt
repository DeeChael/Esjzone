@Composable
fun CatppuccinMacchiatoGreenTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoGreenLightColors
    } else {
        CatppuccinMacchiatoGreenDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

