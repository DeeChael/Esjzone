@Composable
fun CatppuccinMacchiatoRedTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        CatppuccinMacchiatoRedLightColors
    } else {
        CatppuccinMacchiatoRedDarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

